export const getProvinces = (): string[] => {
  return provincesData.map((province) => province.name);
};

export const getAmphoes = (province: string): string[] => {
  const selectedProvince = provincesData.find((p) => p.name === province);
  return selectedProvince ? selectedProvince.amphoes.map((amphoe) => amphoe.name) : [];
};

export const getDistricts = (province: string, amphoe: string): string[] => {
  const selectedProvince = provincesData.find((p) => p.name === province);
  const selectedAmphoe = selectedProvince?.amphoes.find((a) => a.name === amphoe);
  return selectedAmphoe ? selectedAmphoe.districts.map((district) => district.name) : [];
};

export const getZipcode = (province: string, amphoe: string, district: string): string => {
  const selectedProvince = provincesData.find((p) => p.name === province);
  const selectedAmphoe = selectedProvince?.amphoes.find((a) => a.name === amphoe);
  const selectedDistrict = selectedAmphoe?.districts.find((d) => d.name === district);
  return selectedDistrict?.zipcode || "";
};